package org.apache.shindig.gadgets.oauth2.json;

public class OpenSocialData {
	private String openSocialConsumerKey;
	private String openSocialConsumerSecret;

	public String getOpenSocialConsumerKey() {
		return this.openSocialConsumerKey;
	}

	public void setOpenSocialConsumerKey(String openSocialConsumerKey) {
		this.openSocialConsumerKey = openSocialConsumerKey;
	}

	public String getOpenSocialConsumerSecret() {
		return this.openSocialConsumerSecret;
	}

	public void setOpenSocialConsumerSecret(String openSocialConsumerSecret) {
		this.openSocialConsumerSecret = openSocialConsumerSecret;
	}
}
